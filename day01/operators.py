# arithmetic: +, -, *, /, %

print(10 - 2)
print(10 + 2)

print(10 * 3)

print(10 / 3)

print(10 / 2)

print(10 % 3)

print(int(100 / 2))


# shorthand assignment operators: =, +=, -=, *=, /=, %=

a = 100

a = 200

print(a)


a += 100

print(a)

a -= 50

print(a)

a /= 2

print(a)


# logical operators: and, or, not
s = 'Hello World'

print('H' and 'W' in s)

print(True and True)
print(True and False)




